package net.kronos.mclaunch_util_lib.profiles;

public class LauncherVersion
{
	private String name;
	private int format;
	
	/**
	 * @return The name of the version
	 */
	public String getName() {
		return name;
	}
	
	/**
	 * @return The format of the version
	 */
	public int getFormat() {
		return format;
	}
}
